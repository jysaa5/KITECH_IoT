#list 선언
nums = [103, 52, 273, 32, 77]

#최대값 반환 함수: max
max_val = max(nums)

#최소값 반환 함수: min
min_val = min(nums)

#list 요소들의 합을 반환하는 함수: sum
sum_val = sum(nums)

#list 요소들의 평균 값
avg_val = sum_val/len(nums)

print("-----리스트에 사용가능한 함수")
print("-----최대값: ", max_val)
print("-----최소값: ", min_val)
print("-----요소들의 합: ", sum_val)
print("-----요소들의 평균 값:", avg_val)
